// Question analysis functions
export function analyzeQuestionDepth(question: EnhancedGenesisQuestion): {
  cognitive_load: number;
  emotional_intensity: number;
  spiritual_significance: number;
  bonding_potential: number;
} {
  const depthWeights = {
    surface: 1,
    moderate: 2,
    deep: 3,
    profound: 4,
    transcendent: 5
  };

  return {
    cognitive_load: depthWeights[question.depth_level] * question.neural_impact / 10,
    emotional_intensity: question.emotional_weight / 10,
    spiritual_significance: depthWeights[question.depth_level] / 5,
    bonding_potential: question.bonding_potential / 10
  };
}

export function getPhaseQuestions(phase: 'obsidian' | 'leopard' | 'peacock' | 'celestial' | 'void'): EnhancedGenesisQuestion[] {
  return ENHANCED_GENESIS_QUESTIONS.filter(q => q.phase === phase);
}

export function getQuestionById(id: number): EnhancedGenesisQuestion | null {
  return ENHANCED_GENESIS_QUESTIONS.find(q => q.id === id) || null;
}

export function calculateTotalBondingPotential(): number {
  return ENHANCED_GENESIS_QUESTIONS.reduce((sum, q) => sum + q.bonding_potential, 0) / ENHANCED_GENESIS_QUESTIONS.length;
}

export function getMostImpactfulQuestions(count: number = 5): EnhancedGenesisQuestion[] {
  return [...ENHANCED_GENESIS_QUESTIONS]
    .sort((a, b) => b.bonding_potential - a.bonding_potential)
    .slice(0, count);
}

export function getQuestionsByDepth(depth: EnhancedGenesisQuestion['depth_level']): EnhancedGenesisQuestion[] {
  return ENHANCED_GENESIS_QUESTIONS.filter(q => q.depth_level === depth);
}
